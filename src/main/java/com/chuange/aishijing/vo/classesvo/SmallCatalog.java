public class SmallCatalog {
    private String id;
    private String smallChapterTitle;
    private String courseUrl;
    private String videoUrl;
    public SmallCatalog(){};
    public SmallCatalog(String id, String smallChapterTitle, String courseUrl, String videoUrl) {
        this.id = id;
        this.smallChapterTitle = smallChapterTitle;
        this.courseUrl = courseUrl;
        this.videoUrl = videoUrl;
    }

    @Override
    public String toString() {
        return "SmallCatalog{" +
                "id='" + id + '\'' +
                ", smallChapterTitle='" + smallChapterTitle + '\'' +
                ", courseUrl='" + courseUrl + '\'' +
                ", videoUrl='" + videoUrl + '\'' +
                '}';
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSmallChapterTitle() {
        return smallChapterTitle;
    }

    public void setSmallChapterTitle(String smallChapterTitle) {
        this.smallChapterTitle = smallChapterTitle;
    }

    public String getCourseUrl() {
        return courseUrl;
    }

    public void setCourseUrl(String courseUrl) {
        this.courseUrl = courseUrl;
    }

    public String getVideoUrl() {
        return videoUrl;
    }

    public void setVideoUrl(String videoUrl) {
        this.videoUrl = videoUrl;
    }
}
