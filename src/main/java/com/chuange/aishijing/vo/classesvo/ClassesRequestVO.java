public class ClassesRequestVO {
    private String classesType;
    private String price;
    private String score;
    public ClassesRequestVO(){};
    public ClassesRequestVO(String classesType, String price, String score) {
        this.classesType = classesType;
        this.price = price;
        this.score = score;
    }

    @Override
    public String toString() {
        return "ClassesRequestVO{" +
                "classesType='" + classesType + '\'' +
                ", price='" + price + '\'' +
                ", score='" + score + '\'' +
                '}';
    }

    public String getClassesType() {
        return classesType;
    }

    public void setClassesType(String classesType) {
        this.classesType = classesType;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }
}
