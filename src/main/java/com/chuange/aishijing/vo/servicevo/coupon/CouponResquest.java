package com.chuange.aishijing.vo.servicevo.coupon;

import java.util.Date;

/**
 * Created by Administrator on 2018-12-05.
 */
public class CouponResquest {
    private String couponTitle;
    private String status;
    private Date startTime;
    private Date endTime;

    public CouponResquest(){}

    public CouponResquest(String couponTitle, String status, Date startTime, Date endTime) {
        this.couponTitle = couponTitle;
        this.status = status;
        this.startTime = startTime;
        this.endTime = endTime;
    }

    @Override
    public String toString() {
        return "CouponResquest{" +
                "couponTitle='" + couponTitle + '\'' +
                ", status='" + status + '\'' +
                ", startTime=" + startTime +
                ", endTime=" + endTime +
                '}';
    }

    public String getCouponTitle() {
        return couponTitle;
    }

    public void setCouponTitle(String couponTitle) {
        this.couponTitle = couponTitle;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Date getStartTime() {
        return startTime;
    }

    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }

    public Date getEndTime() {
        return endTime;
    }

    public void setEndTime(Date endTime) {
        this.endTime = endTime;
    }
}
