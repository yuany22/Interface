package com.chuange.aishijing.vo.servicevo.index;

import java.util.List;

/**
 * Created by Administrator on 2018-12-17.
 */
public class PermissionSettingResponse {
    private String resourceName;
    private String resourceId;
    private String resourceCode;
    private List<NextTreeResponse> list;

    public PermissionSettingResponse(){}

    public PermissionSettingResponse(String resourceName, String resourceId, String resourceCode, List<NextTreeResponse> list) {
        this.resourceName = resourceName;
        this.resourceId = resourceId;
        this.resourceCode = resourceCode;
        this.list = list;
    }

    @Override
    public String toString() {
        return "PermissionSettingResponseVO{" +
                "resourceName='" + resourceName + '\'' +
                ", resourceId='" + resourceId + '\'' +
                ", resourceCode='" + resourceCode + '\'' +
                ", list=" + list +
                '}';
    }

    public String getResourceName() {
        return resourceName;
    }

    public void setResourceName(String resourceName) {
        this.resourceName = resourceName;
    }

    public String getResourceId() {
        return resourceId;
    }

    public void setResourceId(String resourceId) {
        this.resourceId = resourceId;
    }

    public String getResourceCode() {
        return resourceCode;
    }

    public void setResourceCode(String resourceCode) {
        this.resourceCode = resourceCode;
    }

    public List<NextTreeResponse> getList() {
        return list;
    }

    public void setList(List<NextTreeResponse> list) {
        this.list = list;
    }
}
